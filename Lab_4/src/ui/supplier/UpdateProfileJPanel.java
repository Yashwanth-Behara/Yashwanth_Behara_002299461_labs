    private void attachBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_attachBtnActionPerformed
        // TODO add your handling code here:       ï»¿
        int returnVal = fileChooser.showOpenDialog (this);
        if (returnVal == JFileChooser. APPROVE_OPTION)
        {
            File file = fileChooser.getSelectedFile(); URL url;
            try {
                url = file.toURI().toURL();
                logoImage = new ImageIcon (url);
                logoImage = new ImageIcon(logoImage.getImage().getScaledInstance(150, 150, Image.SCALE_SMOOTH));
                imgContainer.setIcon (logoImage);
            } catch (MalformedURLException ex) {
                Logger.getLogger(this.getName()).log (Level. SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_attachBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        supplier.setLogoImage(logoImage);
        supplier.setSupplyName(nameTxtField.getText());
        JOptionPane.showMessageDialog(null, "Updated profile", "Succes", JOptionPane.INFORMATION_MESSAGE);
        nameTxtField.setText("");
        return;
    }//GEN-LAST:event_updateBtnActionPerformed


